#include <stdio.h>
// relational operators
int main() {
    int a,b;
    printf("enter values of a,b");
    scanf("%d%d",&a,&b);
    printf("a < b is %d \n",a<b);
    printf("a > b is %d \n",a<b);
    printf("a <= b is %d \n",a<=b);
    printf("a >= b is %d \n",a>=b);
    printf("a == b is %d \n",a==b);
    printf("a != b is %d \n",a!=b);
}
