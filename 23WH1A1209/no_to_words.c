#include<stdio.h>
void main()
{
int i;

printf("Enter a number: ");
scanf("\n %d",&i);

switch(i)
{
case 0: printf("\n Zero");
break;
case 1: printf("\n One");
break;
case 2:printf("\n Two");
break;
case 3:printf("\n Three");
break;
case 4:printf("\n Four");
break;
case 5:printf("\n Five");
break;
default: printf("\n The number is not a match.");
}
}
